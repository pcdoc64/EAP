  <b> Risk Matrix </b><br>
  <b>Calculating the inherent risk level for your activity</b>
  <p>The Risk Analysis Matrix below can be used as a guide to assist with quantifying the risk level. To use the matrix, map the likelihood and consequence of an incident occurring with your activity to arrive at the risk level. Keep in mind that when assessing risk value judgements need to be made; and when making value judgements sometimes the risk level is not clear cut. If undecided on a risk level for an activity, a conservative approach would be to settle on the higher risk level being considered. Assessing the risk level is important. However, regardless of the assessed level of risk, we always have an obligation to do what is reasonably practicable to eliminate the risk, or if that is not possible, to minimise the risk to an acceptable level.</p>
